import math

print(2)
print(2.0987)
print(-2.0987)
print(3 + 4.5)
print(3 * 4.5)
print(3 * 4 + 5)
print(3 * (4 + 5))
print(10 % 3) #modulo operator, gives the remainder of the division
my_num = 5
print(my_num)
print(str(my_num))
print(str(my_num) + " my favorite number")
print(abs(my_num)) #absolute value
print(pow(my_num, 2)) #power
print(max(4, 6)) #max value
print(min(4, 6)) #min value
print(round(3.7)) #rounds to the nearest integer
print(math.floor(3.7)) #rounds down to the nearest integer
print(math.ceil(3.7)) #rounds up to the nearest integer
print(math.sqrt(36)) #square root